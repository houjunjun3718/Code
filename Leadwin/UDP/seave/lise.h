#ifndef _LISE_H
#define _LISE_H

#include<stdio.h>
#include<malloc.h>
#include<stdlib.h>

//定义节点结构
typedef struct Node
{
    char *buff;
    struct Node *Next;
}NODE,*PNODE;

//定义队列结构体
typedef struct QNode
{
    PNODE Front,Rear;  //队列头/尾指针
}Queue,*PQueue;

/*************************************************************************
 *创建队列函数
 *参数１：队列头
 *返回值：无返回值
 * **********************************************************************/
void InitQueue(PQueue);

/*************************************************************************
 *判断队列是否为空函数
 *参数１：队列头
 *返回值：队列为空返回0，否则返回1;
 * **********************************************************************/
int IsEmptyQueue(PQueue);

/*************************************************************************
 *入队函数
 *参数１：队头
 *参数２：入队的数据
 *返回值：无返回值
 * **********************************************************************/
void InsertQueue(PQueue,char *data);

/*************************************************************************
 *出队函数
 *参数１：队头
 *参数２：出队的数据
 *返回值：无返回值
 * **********************************************************************/
char *DeleteQueue(PQueue);

/*************************************************************************
 *摧毁队列函数
 *参数１：对头
 *返回值：无返回值
 * **********************************************************************/
void DestroyQueue(PQueue);

/*************************************************************************
 *遍历队列函数
 *参数１：队头
 *返回值：无返回值
 * **********************************************************************/
void TraverseQueue(PQueue);

/*************************************************************************
 *清空队列函数
 *参数１：队头
 *返回值：无返回值
 * **********************************************************************/
void ClearQueue(PQueue);

/*************************************************************************
 *求队列长度函数
 *参数１：对头
 *返回值：返回队列的长度
 * **********************************************************************/
int LengthQueue(PQueue);





#endif
